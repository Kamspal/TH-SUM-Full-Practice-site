                        <a href="/blog/new-site">New Site!</a>
                    </div>
                        <div class="entry">
                            <div>
                                <p>
                                Well it’s been a long minute but we’re finally here! No more  making excuses and frankly, the joke was getting old. We’re excited  about this new direction; a design company with a website, how novel!  Although we have to admit, we’re surprised at how far we got despite not  having one. Thanks to all our amazing clients who trusted us with their  projects enough to overlook our non-website status: We wouldn’t be here  without your support!
                                </p>
                                <p>
                                So now that that’s out of the way, a little Q + A about the new site:
                                </p>
                                <p>
                                <strong>Q:</strong>
                                What’s that swirly cloud thing on the homepage?
                                <br>
                                <strong>A:</strong>
                                It’s a swirly cloud.
                                </p>
                                <p>
                                <strong>Q:</strong>
                                Oh… And what’s with that shadowy character on the site’s footer? He’s a bit scary.
                                <br>
                                <strong>A:</strong>
                                That’s Sasquatch. And he’s not so scary, just misunderstood.
                                </p>
                                <p>
                                <strong>Q:</strong>
                                Why Sasquatch?
                                <br>
                                <strong>A:</strong>
                                : Being based in Vancouver we feel an affinity  with all our Pacific Northwest neighbours, but the sasquatch in  particular holds a very special place in our hearts. Did you know  “sasquatch” means “hairy man”? Which neither of us are. So that’s like,  ironic.
                                </p>
                                <p>
                                <strong>Q:</strong>
                                And have any of you ever seen a sasquatch?
                                <br>
                                <strong>A:</strong>
                                Chris has come the closest. Once while he was  camping in Oregon he thought he saw one. But on closer inspection it  was just a yeti.
                                </p>
                                <p>
                                <strong>Q:</strong>
                                Fair enough. And in your work section, the “OTHER” category..?
                                <br>
                                <strong>A:</strong>
                                That’s where we hope to put any projects that  may not fit into the other categories. We see it being less client  driven and more exploratory in nature. Also, having it there will  hopefully inspire us to fill it with fun stuff. Stay tuned!
                                </p>
                                <p>
                                So it’s been a long time coming, but here we are. We hope you  enjoy the site and check back often for all the latest news and events  going on at The Sum!
                                </p>
